#include<iostream>
using namespace std;

int main(){
    int a = 3,b = 5;
    cout<<(a&b)<<endl;
    cout<<(a|b)<<endl;
    cout<<(~a)<<endl;// 1+1 = 10
    cout<<(a^b)<<endl;
    cout<<(5<<2)<<endl;
    cout<<(28>>3)<<endl;

//increment and decrement 
    cout<<++a<<endl;
    cout<<a++<<endl;
    cout<<++a<<endl;

    cout<<0%2<<endl;
    return 0;

}